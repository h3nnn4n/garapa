#include <stdint.h>
#include <stdlib.h>

#include "types.h"
#include "overlay.h"
#include "graphics_glfw.h"

#define BYTE_TO_BINARY_PATTERN "%c%c%c%c%c%c%c%c"
#define BYTE_TO_BINARY(byte)  \
      (byte & 0x80 ? '1' : '0'), \
  (byte & 0x40 ? '1' : '0'), \
  (byte & 0x20 ? '1' : '0'), \
  (byte & 0x10 ? '1' : '0'), \
  (byte & 0x08 ? '1' : '0'), \
  (byte & 0x04 ? '1' : '0'), \
  (byte & 0x02 ? '1' : '0'), \
  (byte & 0x01 ? '1' : '0')

uint32_t *get_frame_buffer () {
  return get_glfw_frame_buffer();
}

int graphics_init ( ) {
  return glfw_init();
}

void flip_screen ( _cpu_info *cpu ) {
  flip_screen_glfw(cpu);
}

void input_update ( _cpu_info *cpu ) {
  input_update_glfw(cpu);
}

void graphics_exit ( ) {
  glfw_exit();
}
